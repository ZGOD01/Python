#  Following are the basic Math Operation (+ , - , * , / )

#  Addition Operators (+)
a = 10
b = 20
result = a + b
# print(result) Can also do like this 
print(f"Addition of {a} and {b} is: {result}")


#  Subtraction Operators (-)
a = 10
b = 20
result = a - b
print(f"Subtraction of {a} and {b} is: {result}")


#  Multiplication Operators (*)
a = 10
b = 20
result = a * b
print(f"Multiplication of {a} and {b} is: {result}")


#  Division Operators (/)
a = 10
b = 20
result = a / b
print(f"Division of {a} and {b} is: {result}")
